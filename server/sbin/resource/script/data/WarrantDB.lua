--[[WarrantDB.lua
描述:
道具凭证类配置，除了装备，所有不能右键使用的道具都属于凭证
]]
--[[
-- 配置示例：
[10000] =
{
	-- 道具ID，每种大类都有各自的ID段，由策划分配
	ID = 50000,
	-- 道具名字
	Name = 药品测试道具
	-- 道具大类，见server\sbin\resource\script\misc\ItemConstant.lua中的ItemClass
	Class = ItemClass.Medicament,
	-- 道具子类，见server\sbin\resource\script\misc\ItemConstant.lua中的ItemSubClass
	SubClass = ItemSubClass.Warrant,
	-- 最大叠加数目
	MaxPileNum = 1,
	-- 道具品质，见server\sbin\resource\script\misc\ItemConstant.lua中的ItemQuality
	Quality = ItemQuality.White,
	-- 可否交易
	CanTrade = true,
	-- 可否售卖给NPC
	CanSaleNpc = true,
	-- 售卖金钱类型
	SaleMoneyType = ItemPriceType.BindMoney,
	-- 售卖金钱数目
	SaleMoneyNum = 100,
	-- 可否销毁
	CanDestroy = true,
	-- 可否存入仓库
	CanWarehouse = true,
	-- 拥有道具的时间限制，以小时为单位，配置成0代表没有限制时间
	TimeLimitHours = 0,
	-- 拾取音效
	PickUpSound = 0,
},
]]

